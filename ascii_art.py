#!/usr/bin/env python3
"""ASCII art text — render large text banners from font patterns."""
import sys
FONT = {c: [f"{i:05b}".replace('0',' ').replace('1','█') for i in rows] for c, rows in {
    'A': [4,10,17,31,17], 'B': [30,17,30,17,30], 'C': [15,16,16,16,15],
    'D': [30,17,17,17,30], 'E': [31,16,30,16,31], 'F': [31,16,30,16,16],
    'G': [15,16,19,17,15], 'H': [17,17,31,17,17], 'I': [31,4,4,4,31],
    'J': [31,2,2,18,12], 'K': [17,18,28,18,17], 'L': [16,16,16,16,31],
    'M': [17,27,21,17,17], 'N': [17,25,21,19,17], 'O': [14,17,17,17,14],
    'P': [30,17,30,16,16], 'Q': [14,17,21,18,13], 'R': [30,17,30,18,17],
    'S': [15,16,14,1,30], 'T': [31,4,4,4,4], 'U': [17,17,17,17,14],
    'V': [17,17,17,10,4], 'W': [17,17,21,27,17], 'X': [17,10,4,10,17],
    'Y': [17,10,4,4,4], 'Z': [31,2,4,8,31], ' ': [0,0,0,0,0],
    '0': [14,19,21,25,14], '1': [4,12,4,4,14], '2': [14,1,14,16,31],
    '3': [30,1,14,1,30], '4': [17,17,31,1,1], '5': [31,16,30,1,30],
    '6': [15,16,30,17,14], '7': [31,1,2,4,4], '8': [14,17,14,17,14],
    '9': [14,17,15,1,14],
}.items()}

def render(text):
    text = text.upper()
    lines = [""] * 5
    for c in text:
        glyph = FONT.get(c, FONT[' '])
        for i in range(5):
            lines[i] += glyph[i] + " "
    return "\n".join(lines)

if __name__ == "__main__":
    text = " ".join(sys.argv[1:]) or "HELLO"
    print(render(text))
