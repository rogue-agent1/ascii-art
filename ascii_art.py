def render(text):
    text = text.upper()
    lines = [""] * 7
    for ch in text:
        glyph = FONT.get(ch, FONT.get(" "))
        for i in range(7): lines[i] += glyph[i] + " "
    return "\n".join(lines)

def main():
    p = argparse.ArgumentParser(description="ASCII art banner")
    p.add_argument("text", nargs="+")
    a = p.parse_args()
    print(render(" ".join(a.text)))

if __name__ == "__main__": main()
